// OPERADORES DE CADENAS
let cadena1 = `Mi nombre es: `
let cadena2 = `Daniel Torres`
console.log(cadena1 + cadena2);

// Operador de asignación con suma para cadenas de texto
let texto = `Javascript es un lenguaje de `
texto += `tipado dinámico`
console.log(texto);

// Si se concatena un número y una cadena de texto, JS convierte el numero en un texto
let cadena3 = `Hola usuarios ` + 5
console.log(cadena3);

// ejemplo #2
